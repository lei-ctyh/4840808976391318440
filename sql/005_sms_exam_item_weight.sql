-- -------------------------------------------------------------
-- 005 为 sms_exam_item 增加百分比权重字段
-- 用途：在取均值或加权均值场景下，为子项设置权重（0-100）
-- -------------------------------------------------------------

ALTER TABLE `sms_exam_item`
  ADD COLUMN `weight_percent` DECIMAL(5,2) NOT NULL DEFAULT 100.00 COMMENT '百分比权重(0-100)';

-- 说明：
-- 1. 默认 100.00，表示不调整权重；
-- 2. 当父节点使用均值或加权策略时，可基于该字段进行加权计算；
-- 3. 对于合格制项一般不使用该字段，但保留以统一结构；